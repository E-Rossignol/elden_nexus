AshOfWar aow(String name) {
  bool isDlc = ashesOfWar()
          .where((element) => element.name.toUpperCase() == name.toUpperCase())
          .isNotEmpty
      ? false
      : true;
  AshOfWar ash = isDlc
      ? dlcAshesOfWar()
          .where((element) => element.name.toUpperCase() == name.toUpperCase())
          .first
      : ashesOfWar()
          .where((element) => element.name.toUpperCase() == name.toUpperCase())
          .first;
  return ash;
}
